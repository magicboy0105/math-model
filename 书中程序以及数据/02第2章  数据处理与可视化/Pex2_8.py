from numpy import array
x = array([[1,2],[3,4],[5,6]])
print("前两行元素为：\n", x[[0,1]])         #输出：[[1,2],[3,4]]
print("x[0][0]和x[1][1]为：", x[[0,1],[0,1]])    #输出：[1 4]
print("以下两种格式是一样的：")
print(x[[0,1]][:,[0,1]]) # 输出：[[1,2],[3,4]]，
print(x[0:2,0:2])        #同上，输出第1、2行，第1、2列的元素
