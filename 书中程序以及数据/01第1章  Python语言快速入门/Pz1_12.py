d={'姓名':'张三','年龄':33,'子女':{'子':'张四','女':'张玲'}} #构造字典
print(d)  #显示：{'姓名': '张三', '年龄': 33, '子女': {'子': '张四', '女': '张玲'}}
print(d['年龄'])  #显示年龄的值，输出：33
d['年龄']=35  #将字典中'年龄'键的值修改为35
print(d)  #显示修改后字典，输出：{'姓名': '张三', '年龄': 35, '子女': {'子': '张四', '女': '张玲'}}
d['户籍']='烟台'  
print(d)
d.pop('子女')
print(d)
